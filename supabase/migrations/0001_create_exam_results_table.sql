-- Create the exam_results table
create table exam_results (
  id bigint generated by default as identity primary key,
  user_id uuid not null,
  name text not null,
  enrollment_id text not null,
  score float not null,
  correct_answers integer not null,
  incorrect_answers integer not null,
  answers jsonb,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable Row Level Security
alter table exam_results enable row level security;

-- Create policy for inserting data
-- This policy allows anyone to insert a new exam result.
-- For a production app, you would likely want to restrict this to authenticated users.
create policy "Allow public insert"
on exam_results for insert
with check (true);

-- Create policy for reading data
-- This policy allows anyone to read all exam results.
create policy "Allow public read"
on exam_results for select
using (true);
